<#
.SYNOPSIS
    Silently uninstall Biobot Uro software without Python.

.DESCRIPTION
    Step 1 - Uninstall UroBiopsy if installed
    Step 2 - Uninstall UroConnect if installed
    Step 3 - Uninstall UroFusion if installed
    Step 4 - Uninstall UroReview if installed
    Step 5 - Uninstall UroTherapy if installed
    Step 6 - Display status and update log file

.NOTES
    Run as Administrator.
    Default log file: C:\ProgramData\BiobotUroSoftware\Uninstall-Biobot-UroSoftware.log

.EXAMPLES
    powershell.exe -ExecutionPolicy Bypass -NoProfile -File .\Uninstall-Biobot-UroSoftware.ps1
    powershell.exe -ExecutionPolicy Bypass -NoProfile -File .\Uninstall-Biobot-UroSoftware.ps1 -ShowUI
    powershell.exe -ExecutionPolicy Bypass -NoProfile -File .\Uninstall-Biobot-UroSoftware.ps1 -DryRun
#>

param(
    [switch]$ShowUI,
    [switch]$DryRun,
    [int]$TimeoutSeconds = 1800
)

# =========================
# Config
# =========================

$LogDir  = "C:\ProgramData\BiobotUroSoftware"
$LogFile = Join-Path $LogDir "Uninstall-Biobot-UroSoftware.log"

# Most Uninstaller.exe files generated by NSIS use /S for silent uninstall.
# If Biobot's uninstaller uses another silent switch, change SilentArgs below.
$DefaultSilentArgs = "/S"

$Products = @(
    @{
        Name = "UroBiopsy"
        MatchRegex = "(?i)(UroBiopsy|Biobot.*UroBiopsy)"
        KnownUninstallers = @(
            "C:\Program Files\Biobot\UroBiopsy\Uninstaller.exe"
        )
        SilentArgs = $DefaultSilentArgs
        UseNsisWaitPath = $true
    },
    @{
        Name = "UroConnect"
        MatchRegex = "(?i)(UroConnect|Biobot.*UroConnect)"
        KnownUninstallers = @(
            "C:\Program Files\Biobot\UroConnect\Uninstaller.exe"
        )
        SilentArgs = $DefaultSilentArgs
        UseNsisWaitPath = $true
    },
    @{
        Name = "UroFusion"
        MatchRegex = "(?i)(UroFusion|Biobot.*UroFusion)"
        KnownUninstallers = @(
            "C:\Program Files\Biobot\UroFusion\Uninstaller.exe"
        )
        SilentArgs = $DefaultSilentArgs
        UseNsisWaitPath = $true
    },
    @{
        Name = "UroReview"
        MatchRegex = "(?i)(UroReview|Biobot.*UroReview)"
        KnownUninstallers = @(
            "C:\Program Files\Biobot\UroReview\Uninstaller.exe"
